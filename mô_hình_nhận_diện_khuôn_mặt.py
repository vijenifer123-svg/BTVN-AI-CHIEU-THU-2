        "from keras.models import Sequential\n",
        "from keras.layers import Conv2D, MaxPooling2D, Flatten, Dense, Dropout\n",
        "from tensorflow.keras.preprocessing.image import ImageDataGenerator\n",
        "import matplotlib.pyplot as plt\n",
        "\n",
        "# 1. TIỀN XỬ LÝ DỮ LIỆU\n",
        "# Thiết lập đường dẫn dữ liệu\n",
        "train_dir = \"/content/drive/MyDrive/Ảnh selfie cho chiều t2 18 5\"\n",
        "\n",
        "# Kích thước ảnh và kích thước lô\n",
        "img_width, img_height = 128, 128\n",
        "batch_size = 32\n",
        "\n",
        "# Tăng cường dữ liệu dành cho huấn luyện mô hình\n",
        "train_datagen = ImageDataGenerator(\n",
        "    rescale=1.0/255, # Normalize pixel values\n",
        "    rotation_range=30,\n",
        "    width_shift_range=0.2,\n",
        "    height_shift_range=0.2,\n",
        "    shear_range=0.2,\n",
        "    zoom_range=0.2,\n",
        "    horizontal_flip=True,\n",
        "    fill_mode=\"nearest\"\n",
        ")\n",
        "# Tải dữ liệu huấn luyện\n",
        "train_generator = train_datagen.flow_from_directory(\n",
        "    train_dir,\n",
        "    target_size=(img_width, img_height),\n",
        "    batch_size=batch_size,\n",
        "    class_mode=\"categorical\"\n",
        ")\n",
        "\n",
        "# XÂY DỰNG MÔ HÌNH CNN\n",
        "model = Sequential([\n",
        "    Conv2D(32, (3,3), activation=\"relu\", input_shape=(img_width, img_height, 3)),\n",
        "    MaxPooling2D(2,2),\n",
        "    Conv2D(64, (3,3), activation=\"relu\"),\n",
        "    MaxPooling2D(2,2),\n",
        "    Conv2D(128, (3,3), activation=\"relu\"),\n",
        "    MaxPooling2D(2,2),\n",
        "    Flatten(),\n",
        "    Dense(128, activation=\"relu\"),\n",
        "    Dropout(0.5), # Reduce overfitting\n",
        "    Dense(37, activation=\"softmax\") # 5 categories\n",
        "])\n",
        "# Biên dịch mô hình\n",
        "model.compile(optimizer=\"adam\",\n",
        "              loss=\"categorical_crossentropy\",\n",
        "              metrics=[\"accuracy\"])\n",
        "\n",
        "# Tóm tắt cấu hình của mô hình\n",
        "model.summary()\n",
        "\n",
        "# HUẤN LUYÊN MÔ HÌNH CNN\n",
        "epochs = 50\n",
        "history = model.fit(train_generator, epochs=epochs)\n",
        "\n",
        "# ĐÁNH GIÁ KẾT QUẢ MÔ HÌNH\n",
        "plt.plot(history.history['accuracy'], label=\"Kết quả huấn luyện\")\n",
        "# plt.plot(history.history['val_accuracy'], label=\"Độ chính xác xác thực\") # Removed as val_accuracy is not available without validation_data\n",
        "plt.xlabel(\"Số lần học\")\n",
        "plt.ylabel(\"Độ chính xác\")\n",
        "plt.legend()\n",
        "plt.show()\n",
